def promedio_seis():
    n1 = float(input("Ingrese un numero "))
    n2 = float(input("Ingrese un segundo numero "))
    n3 = float(input("Ingrese un tercer numero "))
    n4 = float(input("Ingrese un cuarto numero "))
    n5 = float(input("Ingrese un quinto numero "))
    n6 = float(input("Ingrese un ultimo numero "))

    suma = n1 + n2 + n3 + n4 + n5 + n6

    promedio = 6 / suma

    print("El promedio es:", promedio)

promedio_seis()    